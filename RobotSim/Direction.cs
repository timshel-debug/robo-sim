namespace RobotSim;

/// <summary>
/// Represents the cardinal directions the robot can face.
/// </summary>
public enum Direction
{
    /// <summary>
    /// North direction (increases Y coordinate).
    /// </summary>
    North,
    
    /// <summary>
    /// East direction (increases X coordinate).
    /// </summary>
    East,
    
    /// <summary>
    /// South direction (decreases Y coordinate).
    /// </summary>
    South,
    
    /// <summary>
    /// West direction (decreases X coordinate).
    /// </summary>
    West
}

